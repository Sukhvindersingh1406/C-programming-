#include<stdio.h>
int main()
{
    int day;
    printf("enter the number between 1 to 7");
    scanf("%d",&day);
    switch(day)
    {
        case1:
        printf("\n day 1 is sunday");
        break;
        
        case2:
        printf("\n day 2 is monday");
        break;

        case3:
        printf("\n day 3 is tuesday");
        break;

        case4:
        printf("\n day4 is wednesday");
        break;

        case5 :
        printf("\n day 5 is thursday");
        break;

        case6 :
        printf("\n day 6 is friday");
        break;

        case7:
        printf("\n day 7 is saturday");
        break;

         return 0;

    }
}
